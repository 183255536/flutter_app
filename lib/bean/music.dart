class Song {
  int rank;
  int rankType;
  String rankValue;
  int recType;

  int songId;
  String vid;
  String albumMid;
  String albumId;
  String albumName;
  String albumDesc;
  String id;
  String title;
  String subTitle;
  int songType;
  String albumArtUrl;
  List<SingerBean> singer;
  String strMediaMid;

  String audioPath;
  String lyric;
  String lyricTrans;

  String timePublic;

  String artists;

  Song(
      {this.id,
      this.title,
      this.albumArtUrl,
      this.strMediaMid,
      this.audioPath,
      this.lyric,
      this.lyricTrans,
      this.artists});

  static Song fromMap(Map<String, dynamic> map) {
    if (map == null) return null;
    Song song = Song();
    song.rank = map['rank'];
    song.rankType = map['rankType'];
    song.rankValue = map['rankValue'];
    song.recType = map['recType'];
    song.songId = map['songId'];
    song.vid = map['vid'];
    song.albumMid = map['albumMid'];
    song.title = map['title'];
    song.subTitle = map['subTitle'];
    song.songType = map['songType'];
    song.albumArtUrl = map['albumUrl'];
    song.singer = List()
      ..addAll((map['singer'] as List ?? []).map((o) => SingerBean.fromMap(o)));
    song.id = map['songMid'];
    song.strMediaMid = map['strMediaMid'];
    song.albumDesc = map['albumDesc'];
    song.albumName = map['albumName'];
    song.albumId = "${map['albumId']}";
    song.timePublic = map['time_public'];

    song.audioPath = map['url'];
    song.lyric = map['lyric'];
    song.lyricTrans = map['lyric_trans'];

    song.artists = song.singer[0].name;

    return song;
  }

  Map toJson() => {
        "rank": rank,
        "rankType": rankType,
        "rankValue": rankValue,
        "recType": recType,
        "songId": songId,
        "vid": vid,
        "albumMid": albumMid,
        "title": title,
        "songType": songType,
        "albumUrl": albumArtUrl,
        "songMid": id,
        "subTitle": subTitle,
        "singer": singer,
        "time_public": timePublic,
        "albumId": albumId,
        "albumName": albumName,
        "albumDesc": albumDesc,
        "strMediaMid": strMediaMid,
        "url": audioPath,
        "lyric": lyric,
        "lyric_trans": lyricTrans,
      };
}

class Lyric {
  String lyric;
  Duration startTime;
  Duration endTime;

  Lyric(this.lyric, {this.startTime, this.endTime});

  @override
  String toString() {
    return 'Lyric{lyric: $lyric, startTime: $startTime, endTime: $endTime}';
  }
}


